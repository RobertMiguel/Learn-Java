
public class OperadoresAritimeticos {
	
	public static void main(String[] args) {
		
		//DECLARAÇĂO E INICIALIZAÇĂO DE VARIAVEIS
		
		int x = 10, y =3;
		
		//VARIAS OPERAÇŐES COM AS VARIAVEIS 
		
		System.out.println("X = "+x);
		System.out.println("Y = "+y);
		System.out.println("-X = " + (-x));
		System.out.println("X/Y = " + (x/y));
		System.out.println("Resto de X por Y = "+ (x%y)); // RESULTA 1
		System.out.println("Inteiro de X por Y = " + (int)(x/y));
		System.out.println("X + 1 = " + (++x)); // resulta em 11
	}

}
